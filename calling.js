    // bt-1
      document.getElementById("btn-1").addEventListener('click', function (e) {
        e.preventDefault();
        const coineElement = document.getElementById("coine");
        let coineall = parseInt(coineElement.innerText);
        if (coineall >= 20) {  
          coineall -= 20; 
          coineElement.innerText = coineall;
        //   time or date
          const now = new Date();
          const timeString = now.toLocaleString(); 
          // cart container
          const cartContainer = document.getElementById("cart-container");
          // new div make
          const newcart = document.createElement("div");
          newcart.classList.add("m-2", "bg-gray-200", "p-3", "rounded-lg");
          newcart.innerHTML = `
              <h1 class="font-semibold">Fire Service Number</h1>
              <p>999</p>
              <h1 class="text-gray-600 flex justify-between font-semibold"> Time: ${timeString}</h1>
          `;
          // add to history
          cartContainer.appendChild(newcart);
          alert(' Calling Emergency Number 999...');
        } else {
          alert("Low coin balance!");
        }
      });
